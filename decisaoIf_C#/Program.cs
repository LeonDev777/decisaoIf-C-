//1) Entrar com um número e imprimir uma das mensagens: é múltiplo de 5 ou não é múltiplo de 5.//

class Program
{
    static void Main()
    {
        Console.Write("Digite um número: ");
        int numero = int.Parse(Console.ReadLine());

        if (numero % 5 == 0)
        {
            Console.WriteLine($"O número {numero} é múltiplo de 5.");
        }
        else
        {
            Console.WriteLine($"O número {numero} não é múltiplo de 5.");
        }
    }
}



